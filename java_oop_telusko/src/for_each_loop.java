public class for_each_loop {
    public static void main(String[] args) {
        int num[]=new int [4];
        num[0]=2;
        num[1]=7;
        num[2]=6;
        num[3]=3;

        for (int n:num)//for each loop or enhance for loop
        {
            System.out.println(n);
        }
    }
}
